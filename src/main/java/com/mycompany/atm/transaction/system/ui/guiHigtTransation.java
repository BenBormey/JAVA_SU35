    private void btnPriviewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPriviewActionPerformed
      try {

    java.sql.Connection conn = com.mycompany.atm.transaction.system.DB.dbcontextion.getConnection();

    java.util.HashMap<String, Object> params = new java.util.HashMap<>();
    
 
    if (pickerFrom.getDate() == null || pickerTo.getDate() == null) {
        javax.swing.JOptionPane.showMessageDialog(this, "សូមជ្រើសរើសថ្ងៃខែឱ្យបានត្រឹមត្រូវ!");
        return;
    }
    
    params.put("StartDate", new java.sql.Date(pickerFrom.getDate().getTime()));
    params.put("EndDate", new java.sql.Date(pickerTo.getDate().getTime()));
    InputStream jrxml = getClass().getResourceAsStream("/TopTransactionsReport.jrxml");

    if (jrxml == null) {
        javax.swing.JOptionPane.showMessageDialog(this, "រកមិនឃើញឯកសារ TopTransactionsReport.jrxml ក្នុង Resources ឡើយ!");
        return;
    }

  
    JasperReport report = JasperCompileManager.compileReport(jrxml);
    
 
    JasperPrint print = JasperFillManager.fillReport(report, params, conn);
    
  
    JasperViewer.viewReport(print, false);

        } catch (Exception e) {
            e.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this, "មានបញ្ហាពេលបង្កើត Report: " + e.getMessage());
        }
    }//GEN-LAST:event_btnPriviewActionPerformed

