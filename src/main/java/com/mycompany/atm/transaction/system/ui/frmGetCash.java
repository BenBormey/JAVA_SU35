    private void btnGetCashActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGetCashActionPerformed
         String text = txtAmount.getText().trim();

    if (text.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter amount.");
        return;
    }

    double amount;
    try {
        amount = Double.parseDouble(text);
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Invalid amount.");
        return;
    }

    if (amount <= 0) {
        JOptionPane.showMessageDialog(this, "Amount must be > 0.");
        return;
    }

    // âœ… Open step 2 form with amount
    guiChooseAccount step2 = new guiChooseAccount(this.currentId, amount);
    step2.setLocationRelativeTo(this);
    step2.setVisible(true);

    this.dispose();  // close step1
    }//GEN-LAST:event_btnGetCashActionPerformed

