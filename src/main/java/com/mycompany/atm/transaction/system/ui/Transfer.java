    private void btnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkActionPerformed
 
    // 1. validate input
    if (txtAmount.getText().equals("")) {
        JOptionPane.showMessageDialog(this, "Please Enter Amount");
        return;
    }

    if (txtAccountTo.getText().equals("")) {
        JOptionPane.showMessageDialog(this, "Please Enter Account No");
        return;
    }

    Object selAccount = cboAccount.getSelectedItem();
    if (selAccount == null) {
        JOptionPane.showMessageDialog(this, "Please choose Pay From Account");
        return;
    }

    // 2. convert
    BigDecimal amount = new BigDecimal(txtAmount.getText());
    String toAccountNo = txtAccountTo.getText();

    // 3. parse selected account "123456 - KHR"
    String accountInfo = selAccount.toString();
    String[] parts = accountInfo.split(" - ");
    String accountNo = parts[0].trim();   // sender account no

    // 4. load sender account
    String sqlAcc = """
        SELECT a.id, a.balance, a.is_kh
        FROM account a
        JOIN public."CUSTOMER" c ON a.customer_id = c."ID"
        WHERE a.account_no = ?
          AND c."UserID" = ?
        """;

    var accList = DBHelper.getValues(sqlAcc, accountNo, this.currentUserId);

    if (accList.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Sender account not found!");
        return;
    }

    var acc = accList.get(0);
    long accountIdDb = ((Number) acc.get("id")).longValue();
    BigDecimal balanceBefore = new BigDecimal(acc.get("balance").toString());
    boolean isKh = (Boolean) acc.get("is_kh");

    // 5. check balance
    if (balanceBefore.compareTo(amount) < 0) {
        JOptionPane.showMessageDialog(this, "Not enough balance!");
        return;
    }

    // 6. check receiver exists
    String sqlCheckTo = """
        SELECT id
        FROM account
        WHERE account_no = ?
        """;

    var listTo = DBHelper.getValues(sqlCheckTo, toAccountNo);

    if (listTo.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Receiver account not found!");
        return;
    }

    long toAccountId = ((Number) listTo.get(0).get("id")).longValue();

    // 7. calculate new balance
    BigDecimal balanceAfter = balanceBefore.subtract(amount);

    // 8. insert transfer
    String sqlTransfer = """
        INSERT INTO public.transfer
        (from_account_id, to_account_id, user_id, amount, is_kh, created_at, note)
        VALUES (?, ?, ?, ?, ?, NOW(), ?)
        """;

    int rowTran = DBHelper.execute(
            sqlTransfer,
            accountIdDb,
            toAccountId,
            this.currentUserId,
            amount,
            isKh,
            ""
    );

    if (rowTran == 0) {
        JOptionPane.showMessageDialog(this, "Transfer insert failed");
        return;
    }

    // 9. insert atm_transaction record
    String sqlTrans = """
        INSERT INTO public.atm_transaction(
            account_id,
            user_id,
            tran_type,
            amount,
            is_kh,
            balance_before,
            balance_after,
            created_at,
            note
        )
        VALUES (?, ?, ?, ?, ?, ?, ?, NOW(), ?)
        """;

    int rowsTrans = DBHelper.execute(
            sqlTrans,
            accountIdDb,
            this.currentUserId,
            "TRANSFER",
            amount,
            isKh,
            balanceBefore,
            balanceAfter,
            ""
    );

    if (rowsTrans == 0) {
        JOptionPane.showMessageDialog(this, "atm_transaction insert failed");
        return;
    }

    // 10. update sender balance
    String sqlUpdateSender = """
        UPDATE account
        SET balance = balance - ?
        WHERE id = ?
        """;

    DBHelper.execute(sqlUpdateSender, amount, accountIdDb);

    // 11. update receiver balance
    String sqlUpdateReceiver = """
        UPDATE account
        SET balance = balance + ?
        WHERE id = ?
        """;

    DBHelper.execute(sqlUpdateReceiver, amount, toAccountId);

    // 12. success message
    JOptionPane.showMessageDialog(this, "Transfer success!");


    }//GEN-LAST:event_btnOkActionPerformed
    
  private void loadAccountsToCombo() {

    String sql = """
        SELECT a.account_no      AS account_no,
               a.account_type    AS account_type,
               a.balance         AS balance,
               a.is_kh           AS is_kh
        FROM account a
        JOIN public."CUSTOMER" c
             ON a.customer_id = c."ID"
        WHERE c."UserID" = %d
        """.formatted(currentUserId);

    var list = DBHelper.getValues(sql);
    cboAccount.removeAllItems();

    for (var row : list) {
        String no   = row.get("account_no").toString();
        String type = row.get("account_type").toString();
        double bal  = ((Number) row.get("balance")).doubleValue();
        boolean isKh = (Boolean) row.get("is_kh");   // or (boolean)

        String symbol = isKh ? "áŸ›" : "$";
        String text = "%s - %s (%,.2f %s)".formatted(no, type, bal, symbol);

        cboAccount.addItem(text);
    }
}

