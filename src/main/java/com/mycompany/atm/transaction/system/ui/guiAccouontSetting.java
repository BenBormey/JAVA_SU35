public String GetUserName(int id) {

    String sql = """
        SELECT username
        FROM public.users
        WHERE userid = ?;
        """;

    Object result = DBHelper.executeScalar(sql, id);

    if (result == null) {
        // id not found
        return null;
    }

    return result.toString();
}

