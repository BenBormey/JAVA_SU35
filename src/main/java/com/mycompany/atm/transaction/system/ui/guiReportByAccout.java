    private void comboBoxKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_comboBoxKeyReleased
        // TODO add your handling code here:
        // ដាក់កូដនេះក្នុង Constructor ឬកន្លែង Initialize components
comboBox.setEditable(true); // ត្រូវតែដាក់ឱ្យវាអាចវាយអក្សរបាន (Editable)

JTextField editor = (JTextField) comboBox.getEditor().getEditorComponent();

editor.addKeyListener(new KeyAdapter() {
    @Override
    public void keyReleased(KeyEvent e) {
        // ចាប់យកអក្សរដែលកំពុងវាយ
        String text = editor.getText();
        
        // កុំឱ្យវា Search នៅពេលចុច Arrow Keys (ឡើងលើ ចុះក្រោម)
        if (e.getKeyCode() != KeyEvent.VK_UP && e.getKeyCode() != KeyEvent.VK_DOWN && e.getKeyCode() != KeyEvent.VK_ENTER) {
            searchAccount(text);
            editor.setText(text); // រក្សាទុកអក្សរដែលកំពុងវាយ កុំឱ្យវាបាត់
        }
    }
});
    }//GEN-LAST:event_comboBoxKeyReleased
