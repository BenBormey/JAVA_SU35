public void searchAccount(String text) {
    // លុបទិន្នន័យចាស់ក្នុង ComboBox ចេញសិន
    comboBox.removeAllItems();
    
    // បើប្រអប់ទទេ ឱ្យវាបង្ហាញទាំងអស់ឡើងវិញ
    if (text.isEmpty()) {
        for (String acc : accountList) {
            comboBox.addItem(acc);
        }
    } else {
        // បើមានការវាយអក្សរ ឱ្យវា Filter យកតែពាក្យដែលផ្ដើមដោយអក្សរនោះ (StartsWith)
        for (String acc : accountList) {
            if (acc.toLowerCase().startsWith(text.toLowerCase())) {
                comboBox.addItem(acc);
            }
        }
    }
    
    // បើក Dropdown ឱ្យវាបង្ហាញលទ្ធផល
    comboBox.setPopupVisible(true);
}

