private List<String> accountList = new ArrayList<>();

private void loadingAccount() {
    String sql = "SELECT account_no FROM account";
    
    // ប្រើ DBHelper ដើម្បីទាញយកទិន្នន័យជា ArrayList<HashMap>
    ArrayList<HashMap<String, Object>> data = DBHelper.getValues(sql);
    
    accountList.clear();
    comboBox.removeAllItems();

    for (HashMap<String, Object> row : data) {
        String acc = row.get("account_no").toString();
        accountList.add(acc);
        comboBox.addItem(acc);
    }
}

